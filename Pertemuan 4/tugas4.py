print("Masukkan nilai a = ")
a = input()
print("Masukkan nilai b = ")
b = input()

hasilJumlah = int(a) + int(b)
print("Hasil a + b = " + str(hasilJumlah))

hasilKurang = int(a) - int(b)
print("Hasil a - b = " + str(hasilKurang))

hasilKali = int(a) * int(b)
print("Hasil a * b = " + str(hasilKali))

hasilBagi = int(a) / int(b)
print("Hasil a / b = " + str(hasilBagi))

hasilModulus = int(a) % int(b)
print("Hasil a % b = " + str(hasilModulus))